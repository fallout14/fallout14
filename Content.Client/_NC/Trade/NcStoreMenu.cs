    // #Misfits Change — replaced flat sorted list with per-tier TabContainer tabs + Hall of Fame roster tab
    public void PopulateContracts(List<ContractClientData>? list)
    {
        _lastContracts = list;
        var tierTabs = TierTabs;
        if (tierTabs == null)
            return;

        tierTabs.RemoveAllChildren();

        // Ordered tier definitions (Bronze is always granted on first visit)
        var tierOrder = new[] { "Bronze", "Iron", "Silver", "Gold", "Mithril", "Diamond" };

        // Tier accent colours
        static Color TierColor(string tier) => tier switch
        {
            "Bronze"  => Color.FromHex("#CD7F32"),
            "Iron"    => Color.FromHex("#8C8C8C"),
            "Silver"  => Color.FromHex("#C0C0C0"),
            "Gold"    => Color.FromHex("#FFD700"),
            "Mithril" => Color.FromHex("#4FC3E8"),
            "Diamond" => Color.FromHex("#B9F2FF"),
            _         => Color.FromHex("#9E9E9E")
        };

        // Build one tab per tier
        foreach (var tier in tierOrder)
        {
            var tierContracts = list?.Where(c => c.Difficulty == tier).OrderBy(c => c.Name).ToList()
                                ?? new List<ContractClientData>();
            var unlocked = _playerUnlockedTiers.Contains(tier);

            var scroll = new ScrollContainer
            {
                VerticalExpand = true,
                HorizontalExpand = true
            };
            var col = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                SeparationOverride = 8,
                Margin = new(4, 4, 4, 4)
            };
            scroll.AddChild(col);

            if (!unlocked)
            {
                // Find which tier unlocks this one
                var prevIndex = Array.IndexOf(tierOrder, tier) - 1;
                var prevTier = prevIndex >= 0 ? tierOrder[prevIndex] : "";
                var lockMsg = string.IsNullOrWhiteSpace(prevTier)
                    ? Loc.GetString("nc-contract-tier-locked-first")
                    : Loc.GetString("nc-contract-tier-locked", ("prevTier", prevTier));
                col.AddChild(new Label
                {
                    Text = lockMsg,
                    Modulate = Color.FromHex("#FF6666"),
                    HorizontalAlignment = HAlignment.Center,
                    Margin = new(0, 12, 0, 0)
                });
            }
            else if (tierContracts.Count == 0)
            {
                col.AddChild(new Label
                {
                    Text = Loc.GetString("nc-store-contracts-empty"),
                    Modulate = Color.FromHex("#808080"),
                    HorizontalAlignment = HAlignment.Center,
                    Margin = new(0, 12, 0, 0)
                });
            }
            else
            {
                foreach (var c in tierContracts)
                {
                    var card = new NcContractCard(c, _proto, _sprites, IoCManager.Resolve<IEntityManager>());
                    card.OnClaim += id => OnContractClaim?.Invoke(id);
                    col.AddChild(card);
                }
            }

            tierTabs.AddChild(scroll);
            TabContainer.SetTabTitle(scroll, tier);
        }

        // Hall of Fame tab
        var hofScroll = new ScrollContainer { VerticalExpand = true, HorizontalExpand = true };
        var hofCol = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            SeparationOverride = 4,
            Margin = new(8, 8, 8, 8)
        };
        hofScroll.AddChild(hofCol);

        hofCol.AddChild(new Label
        {
            Text = Loc.GetString("nc-store-tab-tier-hall-of-fame"),
            StyleClasses = { "LabelHeading" },
            Margin = new(0, 0, 0, 8)
        });

        if (_roster.Count == 0)
        {
            hofCol.AddChild(new Label
            {
                Text = Loc.GetString("nc-store-hall-of-fame-empty"),
                Modulate = Color.FromHex("#808080"),
                HorizontalAlignment = HAlignment.Center
            });
        }
        else
        {
            // Sort by tier rank descending, then by name
            var tierRank = new Dictionary<string, int>
            {
                { "Bronze", 0 }, { "Iron", 1 }, { "Silver", 2 },
                { "Gold", 3 }, { "Mithril", 4 }, { "Diamond", 5 }
            };

            foreach (var entry in _roster
                         .OrderByDescending(e => tierRank.GetValueOrDefault(e.HighestTier, -1))
                         .ThenBy(e => e.PlayerName))
            {
                var entryColor = TierColor(entry.HighestTier);
                var row = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Horizontal,
                    SeparationOverride = 8
                };

                row.AddChild(new Label
                {
                    Text = entry.PlayerName,
                    HorizontalExpand = true,
                    Modulate = Color.White
                });

                row.AddChild(new Label
                {
                    Text = entry.HighestTier,
                    Modulate = entryColor
                });

                row.AddChild(new Label
                {
                    Text = Loc.GetString("nc-store-hall-of-fame-completed", ("count", entry.TotalCompleted)),
                    Modulate = Color.FromHex("#A0A0A0")
                });

                hofCol.AddChild(row);
            }
        }

        tierTabs.AddChild(hofScroll);
        TabContainer.SetTabTitle(hofScroll, Loc.GetString("nc-store-tab-tier-hall-of-fame"));

        ApplyTabsVisibility();
    }

    private void RefreshListings()
    {
        var items = _catalogModel.Items;
        BuyView.BindListingsData(items, GetBalanceForCurrency, (d, qty) => OnBuyPressed?.Invoke(d, qty));
        SellView.BindListingsData(items, static _ => int.MaxValue, (d, qty) => OnSellPressed?.Invoke(d, qty));

        BuyView.SetSearch(_searchLower);
        SellView.SetSearch(_searchLower);
    }

    private void BuildCategoryButtons()
    {
        BuyView.SetCategories(_buyCats);
        SellView.SetCategories(_sellCats);
    }

    private string CurrencyName(string? currencyId)
    {
        if (string.IsNullOrWhiteSpace(currencyId))
            return string.Empty;

        if (_proto.TryIndex<StackPrototype>(currencyId, out var stackProto) &&
            _proto.TryIndex<EntityPrototype>(stackProto.Spawn, out var currencyEnt))
            return currencyEnt.Name;

        return currencyId;
    }

    [Obsolete("Controls should only be removed from UI tree instead of being disposed")]
    protected override void Dispose(bool disposing)
    {
        _disposed = true;
        BuyView.ClearCaches();
        SellView.ClearCaches();
        _catalogModel.Clear();

        base.Dispose(disposing);
    }
